//  NÚMEROS DE 1 A 10

document.write('<h3>Números - 1 à 10</h3>');
let ini = 1;

while (ini <= 10) {
    document.write(ini,' ');
    ini += 1;   
}

document.write('<hr>');

//NÚMEROS DE 50 A 1

document.write('<h3>Números - 50 à 1</h3>');
ini = 50;

while (ini >= 1) {
    document.write(ini,' ');
    ini -= 1;   
}

document.write('<hr>');

//NÚMEROS PARES DE 1 A 100

document.write('<h3>Números Pares - 0 à 100</h3>');
ini = 0;

while (ini <= 100) {
    document.write(ini,' ');
    ini += 2;   
}

document.write('<hr>');

//NÚMEROS PARES DE 1 A 100

document.write('<h3>Números Ímpares - 99 à 1</h3>');
ini = 99;

while (ini >= 1) {
    document.write(ini,' ');
    ini -= 2;   
}

document.write('<hr>');